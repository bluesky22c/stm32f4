/*******************************************************************************
* Function Name  : Mutex_Lock
* Description    : disable Inturrupt 
* Input          : None.
* Return         : None.
*******************************************************************************/
void Mutex_Lock(void)
{
  __disable_irq();
}

/*******************************************************************************
* Function Name  : Mutex_UnLock
* Description    : Enable Inturrupt 
* Input          : None.
* Return         : None.
*******************************************************************************/
void Mutex_UnLock(void)
{
  __enable_irq();
}

